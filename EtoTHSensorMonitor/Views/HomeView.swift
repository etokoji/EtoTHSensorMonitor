                                        title: "ÊπøÂ∫¶",
                                        value: data.formattedHumidity,
                                        icon: "humidity",
                                        color: .blue,
                                        isCompact: false,
                                        isLargeScreen: isLargeScreenDynamic,
                                        isCompactDevice: isCompactDeviceDynamic
                                    )
                                }
                                
                                HStack(spacing: isCompactDeviceDynamic ? 15 : (isLargeScreenDynamic ? 25 : (isLandscape ? 12 : 20))) {
                                    SensorCard(
                                        title: "Ê∞óÂúß",
                                        value: data.formattedPressure,
                                        icon: "barometer",
                                        color: .orange,
                                        isCompact: false,
                                        isLargeScreen: isLargeScreenDynamic,
                                        isCompactDevice: isCompactDeviceDynamic
                                    )
                                    
                                    SensorCard(
                                        title: "ÈõªÂúß",
                                        value: data.formattedVoltage,
                                        icon: "battery.100",
                                        color: .green,
                                        isCompact: false,
                                        isLargeScreen: isLargeScreenDynamic,
                                        isCompactDevice: isCompactDeviceDynamic
                                    )
                                }
                            }
                            // ÊúÄÂ§ßÂπÖÂà∂Èôê„ÇíÂâäÈô§„Åó„ÄÅ„Ç¶„Ç£„É≥„Éâ„Ç¶ÂπÖ„Å´ÊØî‰æã„Åó„Å¶„Ç´„Éº„Éâ„ÅåÊã°„Åå„Çã
