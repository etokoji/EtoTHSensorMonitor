                            Image(systemName: "sensors")
                                .font(.system(size: 60))
                                .foregroundColor(.gray)
                            
                            Text("„Çª„É≥„Çµ„Éº„Éá„Éº„Çø„ÇíÊ§úÁ¥¢‰∏≠...")
