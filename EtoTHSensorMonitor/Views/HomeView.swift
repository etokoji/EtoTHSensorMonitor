        return formatter.string(from: date)
    }
}

struct SensorCard: View {
    let title: String
    let value: String
    let icon: String
    let color: Color
    var isCompact: Bool = false
    var isLargeScreen: Bool = false
    var isCompactDevice: Bool = false
    
    var body: some View {
        if isCompact {
            // Compact layout for landscape/grid view - horizontal like portrait but smaller
            HStack(spacing: 15) {
                // Icon - smaller than portrait
                Image(systemName: icon)
                    .font(.system(size: 24))
                    .foregroundColor(color)
                    .frame(width: 45, height: 45)
                    .background(color.opacity(0.1))
                    .clipShape(Circle())
                
                // Content
                VStack(alignment: .leading, spacing: 2) {
                    Text(title)
                        .font(.subheadline)
                        .foregroundColor(.secondary)
                    
                    Text(value)
                        .font(.system(size: 32, weight: .bold, design: .rounded))
                        .foregroundColor(.primary)
                        .lineLimit(1)
                        .minimumScaleFactor(0.7)
                }
                
                Spacer()
            }
            .frame(maxWidth: .infinity, minHeight: 60)
            .padding(.horizontal, 15)
            .padding(.vertical, 10)
            .background(
                RoundedRectangle(cornerRadius: 12)
                    .fill(Color(.systemBackground))
                    .shadow(color: Color.black.opacity(0.1), radius: 3, x: 0, y: 1)
            )
        } else {
            // Full layout for portrait view and large screens
            HStack(spacing: isCompactDevice ? 15 : 20) {
                // Icon - „Ç≥„É≥„Éë„ÇØ„Éà„Éá„Éê„Ç§„Çπ„Åß„ÅØÂ∞ë„ÅóÂ§ß„Åç„Åè
                Image(systemName: icon)
                    .font(.system(size: isCompactDevice ? 35 : 30))
                    .foregroundColor(color)
                    .frame(width: isCompactDevice ? 70 : 60, height: isCompactDevice ? 70 : 60)
                    .background(color.opacity(0.1))
                    .clipShape(Circle())
                
                // Content
                VStack(alignment: .leading, spacing: isCompactDevice ? 8 : 5) {
                    Text(title)
                        .font(isCompactDevice ? .title3 : .headline)
                        .foregroundColor(.secondary)
                    
                    Text(value)
                        .font(.system(size: isCompactDevice ? 38 : (isLargeScreen ? 44 : 40), weight: .bold, design: .rounded)) // Â§ßÁîªÈù¢„Åß‰∏ÄÁï™Â§ß„Åç„Åè„ÄÅÊ¨°„Å´iPhone„ÄÅ„Ç≥„É≥„Éë„ÇØ„Éà„Éá„Éê„Ç§„Çπ
                        .foregroundColor(.primary)
                        .lineLimit(1)
                        .minimumScaleFactor(0.6) // Mac„Åß„ÅÆË°®Á§∫„ÇíÊîπÂñÑ„Åô„Çã„Åü„ÇÅ„Å´Á∏ÆÂ∞èÁéá„Çí‰∏ä„Åí„Çã
                        .allowsTightening(true) // ÊñáÂ≠óÈñìÈöî„ÅÆÁ∑†„ÇÅ„ÇíË®±ÂèØ
                }
                
                Spacer()
            }
            .frame(maxWidth: .infinity, minHeight: isCompactDevice ? 100 : 80) // „Ç≥„É≥„Éë„ÇØ„Éà„Éá„Éê„Ç§„Çπ„Åß„ÅØ„Ç´„Éº„Éâ„ÇíÂ§ß„Åç„Åè
            .padding(.horizontal, isCompactDevice ? 18 : 15) // „Ç≥„É≥„Éë„ÇØ„Éà„Éá„Éê„Ç§„Çπ„Åß„ÅØÂ∞ë„ÅóÂ§ß„Åç„Å™„Éë„Éá„Ç£„É≥„Ç∞
            .padding(.vertical, isCompactDevice ? 18 : 15)
            .background(
                RoundedRectangle(cornerRadius: isCompactDevice ? 18 : 15)
                    .fill(Color(.systemBackground))
                    .shadow(color: Color.black.opacity(0.1), radius: isCompactDevice ? 6 : 5, x: 0, y: 2)
            )
        }
    }
}

